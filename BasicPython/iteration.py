# This code demonstrates the use of a while loop to print even numbers from 0 to 20.
# It initializes a variable x to 0 and increments it by 2 in each iteration until it reaches 20.
print("\nPrinting even numbers from 0 to 20 using a while loop:")
x=0
while x<=20:
    print(x,end=" ")
    x = x + 2

